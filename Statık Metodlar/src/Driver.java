
public class Driver {
	public static void main(String[] args) {
		System.out.println(Math.PI);  
	System.out.println(Math.pow(2,3)); // 2 nin kübünü bize verir .
	
	System.out.println(Math.sqrt(9)); // istediğimiz bir sayının karekökünü bulmak istiyorsak 
	System.out.println(Math.min(6, 3)); // kendisine verilen değişkenlerden küçük olanı bize geri döndürür 
	
	System.out.println(Math.max (6,3)); // kendisine verilen değerlerden büyük olanı  bize geri dönderir 
	
	float f =1.6f;
	System.out.println(Math.ceil(f));  // ceil metodu kendisine verilen değeri yukarı yuvarlayarak ekrana yazdırır .
	System.out.println((Math.floor(f))); // floor kendisine verilen değeri aşağı yuvarlayarak ekrana yazdırır.
	System.out.println(Math.round(f)); // eğer sayı bucuktan kucukse bucuk hariç aşağı yuvarlar ,buyukse yukarı yuvarlar .
	System.out.println(Math.random()*10); // sıfır dahil 1 hariç sıfır ile bir arasında rastgele bir sayı üretir 
	 // 10 ile çarparsak 0  0 dahil  ile 10 arasında rastgeele bir sayı üretecek
	
	}

}
